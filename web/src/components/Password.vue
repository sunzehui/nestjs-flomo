<script lang="ts" setup>
import { ref, watch } from "vue";
import { Field as VField, ErrorMessage } from "vee-validate";
const props = defineProps<{
  modelValue: string;
  name: string;
  another: string;
}>();
const emit = defineEmits(["update:modelValue"]);
const password = ref(props.modelValue);
watch(password, (value) => {
  emit("update:modelValue", value);
});
const passwordRule = (password) => {
  const notEmpty = password.length > 0;
  const isValid = /(?=^.{6,}$).*$/.test(password);

  if (props.another) {
    // 如果两个密码不一致，则报错
    return notEmpty && isValid && password === props.another;
  }
  return notEmpty && isValid;
};
const pwdShow = ref(false);
</script>
<template>
  <div class="relative">
    <label :for="props.name" class="sr-only">密码</label>
    <VField
      :name="props.name"
      :type="pwdShow ? 'text' : 'password'"
      autocomplete="current-password"
      :rules="passwordRule"
      v-model="password"
      class="flomo-input on-active"
      placeholder="密码"
    />

    <ErrorMessage
      :name="props.name"
      class="text-red-500 text-xs italic"
    ></ErrorMessage>
    <div class="eye_open" @click="pwdShow = false" v-show="pwdShow">
      <svg
        xmlns="http://www.w3.org/2000/svg"
        fill="none"
        viewBox="0 0 24 24"
        stroke="gray"
        stroke-width="2"
      >
        <path
          stroke-linecap="round"
          stroke-linejoin="round"
          d="M15 12a3 3 0 11-6 0 3 3 0 016 0z"
        />
        <path
          stroke-linecap="round"
          stroke-linejoin="round"
          d="M2.458 12C3.732 7.943 7.523 5 12 5c4.478 0 8.268 2.943 9.542 7-1.274 4.057-5.064 7-9.542 7-4.477 0-8.268-2.943-9.542-7z"
        />
      </svg>
    </div>

    <div class="eye_close" @click="pwdShow = true" v-show="!pwdShow">
      <svg
        xmlns="http://www.w3.org/2000/svg"
        class="h-6 w-6"
        fill="none"
        viewBox="0 0 24 24"
        stroke="gray"
        stroke-width="2"
      >
        <path
          stroke-linecap="round"
          stroke-linejoin="round"
          d="M13.875 18.825A10.05 10.05 0 0112 19c-4.478 0-8.268-2.943-9.543-7a9.97 9.97 0 011.563-3.029m5.858.908a3 3 0 114.243 4.243M9.878 9.878l4.242 4.242M9.88 9.88l-3.29-3.29m7.532 7.532l3.29 3.29M3 3l3.59 3.59m0 0A9.953 9.953 0 0112 5c4.478 0 8.268 2.943 9.543 7a10.025 10.025 0 01-4.132 5.411m0 0L21 21"
        />
      </svg>
    </div>
  </div>
</template>
<style lang="scss">
.eye_open,
.eye_close {
  @apply absolute top-1/2 right-2 h-full w-8 -translate-y-1/2;
  svg {
    @apply w-4 h-4 absolute top-1/2 right-2 -translate-y-1/2;
  }
}
.flomo-input {
  @apply appearance-none relative block w-full px-3 py-2 border border-gray-200 placeholder-gray-300 text-gray-900 rounded-md sm:text-sm h-10;
  &:on-active {
    @apply focus:outline-white focus:ring-indigo-500 focus:border-indigo-500 focus:z-10;
  }
}
</style>
